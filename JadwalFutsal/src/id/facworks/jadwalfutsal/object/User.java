package id.facworks.jadwalfutsal.object;

public class User {

	String user, nohp;

	public User(String user, String nohp) {

		this.user = user;
		this.nohp = nohp;
	}

	public String getuser() {
		return user;
	}

	public void setuser(String user) {
		this.user = user;
	}

	public String getnohp() {
		return nohp;
	}

	public void setnohp(String nohp) {
		this.nohp = nohp;
	}

}